  const params: (keyof Habitat)[] = ['temp', 'grav', 'atmo', 'rad', 'humi', 'res'];
  let totalDiff = 0;
  params.forEach(p => {
    totalDiff += Math.abs(raceHabitat[p] - planetHabitat[p]);
  });
